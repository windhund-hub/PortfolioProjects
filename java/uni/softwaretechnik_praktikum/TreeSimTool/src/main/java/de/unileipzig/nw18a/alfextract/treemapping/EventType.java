package de.unileipzig.nw18a.alfextract.treemapping;

/**
 * Represents one of the events speciation, duplication or lateral gene transfer.
 */
public enum EventType {
  /**
   * Speciation event.
   */
  SPEC,
  /**
   * Duplication event.
   */
  DUP,
  /**
   * Lateral gene transfer event.
   */
  LGT
}
